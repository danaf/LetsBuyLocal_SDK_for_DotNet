namespace LetsBuyLocal.SDK.Models
{
    /// <summary>
    /// Represents an error message.
    /// </summary>
    public class ErrorMessage
    {
        /// <summary>
        /// Gets or sets the title.
        /// </summary>
        /// <value>
        /// The title.
        /// </value>
        public string Title { get; set; }
        /// <summary>
        /// Gets or sets the description.
        /// </summary>
        /// <value>
        /// The description.
        /// </value>
        public string Description { get; set; }
    }
}
